#include "GeneradorDeFigurasCompuestas.h"

Balancin* GeneradorDeFigurasCompuestas::construirBalancin(double posX, double posY, double angulo){

	Figura* tabla = new Figura(ID_TABLA_BALANCIN, new Cuadrado(ANCHO_BALANCIN,ALTO_BALANCIN,posX,posY,angulo));
	double cirXder, cirXizq, cirY;

	Balancin::calcularPosPuntas(&cirXizq,&cirXder,&cirY,posX,posY);

	Figura* circDer = new Figura(ID_PUNTA_BALANCIN, new Circulo(RADIO_PUNTA_BALANCIN,cirXder,cirY,angulo));
	Figura* circIzq = new Figura(ID_PUNTA_BALANCIN, new Circulo(RADIO_PUNTA_BALANCIN,cirXizq,cirY,angulo));

	std::list<Figura*> listaFig;

	listaFig.push_back(tabla);
	listaFig.push_back(circDer);
	listaFig.push_back(circIzq);

	Balancin* balancin = new Balancin(posX,posY,angulo,listaFig);

	return balancin;
}
